import UIKit

class ApplicationColor {
    static func darkPrimaryColor() -> UIColor {
        return UIColor(red: 56/255, green: 142/255, blue: 60/255, alpha: 1)
    }
    
    static func primaryColor() -> UIColor {
        return UIColor(red: 76/255, green: 175/255, blue: 80/255, alpha: 1)
    }
    
    static func lighyPrimaryColor() -> UIColor {
        return UIColor(red: 200/255, green: 230/255, blue: 201/255, alpha: 1)
    }
    
    static func textColor() -> UIColor {
        return UIColor.whiteColor()
    }
    
    static func accentColor() -> UIColor {
        return UIColor(red: 255/255, green: 82/255, blue: 82/255, alpha: 1)
    }
    
    static func primaryText() -> UIColor {
        return UIColor(red: 33/255, green: 33/255, blue: 33/255, alpha: 1)
    }
    
    static func secondaryText() -> UIColor {
        return UIColor(red: 114/255, green: 114/255, blue: 114/255, alpha: 1)
    }
    
    static func dividerText() -> UIColor {
        return UIColor(red: 182/255, green: 182/255, blue: 182/255, alpha: 1)
    }
}